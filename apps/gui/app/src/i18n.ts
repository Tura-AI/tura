export type TextKey = keyof (typeof dictionaries)["zh-CN"];

export const activeLanguage: Language = "zh-CN";

export function t(
  key: TextKey,
  values?: Record<string, string | number>,
): string {
  const template: string =
    dictionaries[activeLanguage][key] ?? dictionaries.en[key];
  if (!values) {
    return template;
  }
  let text = template;
  for (const [name, value] of Object.entries(values)) {
    text = text.replaceAll(`{${name}}`, String(value));
  }
  return text;
}

export function assertDictionaryParity(): void {
  const zh = Object.keys(dictionaries["zh-CN"]).sort();
  const en = Object.keys(dictionaries.en).sort();
  if (zh.join("\n") !== en.join("\n")) {
    throw new Error(
      "GUI i18n dictionaries must keep zh-CN and en keys in sync",
    );
  }
}
